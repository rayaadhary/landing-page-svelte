<h2>Kapan Pilih SIMRS?</h2>
<p>Pilih SIMRS jika institusi Anda:</p>
<ul>
<li>✅ Berstatus Rumah Sakit (kelas D pratama hingga A)</li>
<li>✅ Memiliki unit IGD, Rawat Inap, dan OK</li>
<li>✅ Wajib laporan akreditasi KARS / JCI</li>
<li>✅ Butuh integrasi penuh dengan SatuSehat dan VClaim BPJS</li>
</ul>

<h2>Kapan Pilih Aplikasi Klinik?</h2>
<p>Pilih Aplikasi Klinik jika:</p>
<ul>
<li>✅ Anda mengelola klinik pratama / utama</li>
<li>✅ Fokus pada pelayanan rawat jalan dan konsultasi</li>
<li>✅ Butuh sistem yang cepat implementasi dan mudah digunakan</li>
<li>✅ Budget terbatas tetapi tetap ingin digitalisasi</li>
</ul>

