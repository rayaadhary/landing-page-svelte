<h2>Masalah yang Diselesaikan HRIS</h2>

<ul>
<li>❌ <strong>Rekap absensi manual:</strong> HR habis 3-5 hari kerja setiap bulan untuk merekap — HRIS otomatis hitung di menit yang sama</li>
<li>❌ <strong>Kesalahan hitung gaji:</strong> Satu baris Excel kelewat bisa bikin salah bayar puluhan karyawan</li>
<li>❌ <strong>Cuti &amp; izin tidak terdokumentasi:</strong> Karyawan ngaku sudah izin, atasan lupa — HRIS mencatat semua dengan approval trail</li>
<li>❌ <strong>Data karyawan tersebar:</strong> Kontrak di sini, absensi di sana, gaji di file lain — HRIS menyatukan semuanya</li>
<li>❌ <strong>Laporan SDM lambat:</strong> Direksi minta data turnover 1 jam lagi — HRIS generate dalam hitungan detik</li>
</ul>

