<h2>Faktor yang Mempengaruhi Biaya</h2>
<ol>
<li><strong>Kompleksitas fitur</strong> — semakin banyak integrasi (API, payment gateway, BPJS), semakin tinggi biaya</li>
<li><strong>Jenis pengguna</strong> — aplikasi internal (dashboard admin) lebih murah dari aplikasi publik (ribuan user simultan)</li>
<li><strong>Infrastruktur</strong> — cloud hosting vs on-premise server</li>
<li><strong>Maintenance</strong> — apakah termasuk update fitur berkala? support 24/7?</li>
</ol>

